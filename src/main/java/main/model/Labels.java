package main.model;

public class Labels {

    private String jegy;
    private String kredit;

    public Labels(String jegy, String kredit) {
        this.jegy = jegy;
        this.kredit = kredit;
    }

    public String getJegy() {
        return jegy;
    }

    public String getKredit() {
        return kredit;
    }

}
